def render_nav(current_slug, current_cat):
    items = []
    for entry in NAV_DATA:
        title, cat_id, link_to, dropdown = entry
        is_active = (cat_id == current_cat)
        active_class = ' class="active"' if is_active else ''
        href = link_to if link_to else '#'
        if dropdown is None:
            items.append(f'      <li{active_class}><a href="{href}">{title}</a></li>')
        else:
            drop_items = []
            for slug, name in dropdown:
                cur_class = ' class="current"' if slug == current_slug else ''
                drop_items.append(f'          <li><a href="{slug}.html"{cur_class}>{name}</a></li>')
            drop_html = '\n'.join(drop_items)
            items.append(
                f'      <li{active_class}>\n'
                f'        <a href="{href}">{title}</a>\n'
                f'        <ul class="dropdown">\n{drop_html}\n        </ul>\n'
                f'      </li>'
            )
    return '\n'.join(items)


